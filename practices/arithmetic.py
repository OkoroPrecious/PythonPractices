number = int(input("Enter first number: "))
user = int(input("Enter second number: "))

first_square = number * number
second_square = user * user
subtraction = first_square - second_square
addition = first_square + second_square

print("Square of first number is: ", first_square)
print("Square of second number is: ", second_square)
print("subtraction of squared numbers is: ", subtraction)
print("Addition of squared numbers is: ", addition)





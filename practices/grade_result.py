score = int(input("Enter a score: "))
if 70 <= score <= 100:
    print("Grade is A")
if 60 <= score <= 69:
    print("Grade is B")
if 50 <= score <= 59:
    print("Grade is C")
if 45 <= score <= 49:
    print("Grade is D")
if 40 <= score <= 44:
    print("Grade is E")
else:
    print("Grade is F")

